    public static int[] mezcla_directa(int[] array) {
      if (array.length > 1) {
         int i,j,k;
         int ElmIzq = array.length / 2, ElmDer = array.length - ElmIzq;
         int ArrIzq[] = new int[ElmIzq], ArrDer[] = new int[ElmDer];
         for (i = 0; i < ElmIzq; i++)
            ArrIzq[i] = array[i];
         for (i = ElmIzq; i < ElmIzq + ElmDer; i++)
            ArrDer[i-ElmIzq] = array[i];
         ArrIzq = mezcla_directa(ArrIzq);

         ArrDer = mezcla_directa(ArrDer);

         i=0;j=k=0;
         while(ArrIzq.length != j && ArrDer.length !=k)
            if(ArrIzq[j]<ArrDer[k]) array[i++] = ArrIzq[j++];
            else array[i++] = ArrDer[k++];
         while(ArrIzq.length != j) array[i++] = ArrIzq[j++];
         while(ArrDer.length != k) array[i++] = ArrDer[k++];

      }
        return array;

        
    }
    
    public static void mezcla_natural(int[] array){
        int Izquierda, izq=0, Derecha =array.length-1, der=Derecha;
        boolean ordenado;
        do{
            ordenado = true;
            Izquierda =0;
            while(Izquierda < Derecha){
                izq = Izquierda;
                while(izq<Derecha && array[izq] <=array[izq+1]) izq++;
                der=izq+1;
                while(der == Derecha-1 || der < Derecha && array[der]<=array[der+1]) der++;
                if(der<=Derecha) {
                    mezcla_directa(array);
                    ordenado=false;
                }
                Izquierda = izq;
            }
        }while(!ordenado);
        
    }
    
